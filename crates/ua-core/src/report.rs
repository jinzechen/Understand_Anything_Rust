// ── 2. Layers ────────────────────────────────────────────────────────────────

fn html_section_layers(out: &mut String, graph: &KnowledgeGraph) {
    html_section_open(out, "layers", "架构分层");

    let node_map: HashMap<&str, &GraphNode> =
        graph.nodes.iter().map(|n| (n.id.as_str(), n)).collect();

    // Color palette for layers
    let layer_colors = [
        ("code", "#4fc3f7", "#0288d1"),
        ("config", "#fff176", "#f9a825"),
        ("docs", "#a5d6a7", "#388e3c"),
        ("infra", "#ef9a9a", "#c62828"),
        ("data", "#ce93d8", "#7b1fa2"),
        ("api", "#ffcc80", "#e65100"),
        ("scripts", "#b0bec5", "#455a64"),
    ];

    for layer in &graph.layers {
        let (bg, border) = layer_colors
            .iter()
            .find(|(id, ..)| *id == layer.id)
            .map(|(_, bg, border)| (*bg, *border))
            .unwrap_or(("#424242", "#616161"));

        out.push_str(&format!(
            "<div class=\"layer-card\" style=\"border-left: 4px solid {}; background: linear-gradient(135deg, {}22 0%, {}08 100%);\">\n",
            border, bg, bg
        ));
        out.push_str(&format!(
            "<h3><span class=\"layer-dot\" style=\"background:{}\"></span>{} <span class=\"layer-count\">({} items)</span></h3>\n",
            border,
            esc(&layer.name),
            layer.node_ids.len()
        ));
        out.push_str(&format!(
            "<p class=\"layer-desc\">{}</p>\n",
            esc(&layer.description)
        ));

        if !layer.node_ids.is_empty() {
            out.push_str("<ul class=\"layer-nodes\">\n");
            for nid in &layer.node_ids {
                if let Some(node) = node_map.get(nid.as_str()) {
                    let label = node.file_path.as_deref().unwrap_or(&node.name);
                    out.push_str(&format!(
                        "<li id=\"node-{}\">{}</li>\n",
                        esc(&node.id),
                        esc(label)
                    ));
                }
            }
            out.push_str("</ul>\n");
        }
        out.push_str("</div>\n");
    }

    html_section_close(out);
}

// ── 3. File Inventory ────────────────────────────────────────────────────────

fn html_section_file_inventory(out: &mut String, graph: &KnowledgeGraph) {
    html_section_open(out, "files", "文件清单");

    let file_nodes: Vec<&GraphNode> = graph
        .nodes
        .iter()
        .filter(|n| n.file_path.is_some())
        .collect();

    out.push_str("<div class=\"table-wrap\"><table class=\"file-table\">\n");
    out.push_str(
        "<thead><tr><th>路径</th><th>语言</th><th>行数</th><th>类别</th></tr></thead>\n<tbody>\n",
    );

    for node in &file_nodes {
        let path = node.file_path.as_deref().unwrap_or("-");
        let language = node.tags.first().map(|s| s.as_str()).unwrap_or("-");

        // Extract line count from summary (e.g., "rust (150 lines, 2 definitions, 1 imports)")
        let lines = extract_line_count(&node.summary).unwrap_or("?");

        let category = format!("{:?}", node.node_type);

        out.push_str(&format!(
            "<tr><td class=\"path-col\">{}</td><td><span class=\"lang-badge\">{}</span></td><td class=\"num\">{}</td><td>{}</td></tr>\n",
            esc(path),
            esc(language),
            lines,
            esc(&category)
        ));
    }

    out.push_str("</tbody>\n</table></div>\n");
    html_section_close(out);
}

// ── 4. Import Relationships ──────────────────────────────────────────────────

fn html_section_imports(out: &mut String, graph: &KnowledgeGraph) {
    let import_edges: Vec<&GraphEdge> = graph
        .edges
        .iter()
        .filter(|e| e.edge_type == EdgeType::Imports)
        .collect();

    html_section_open(out, "imports", "导入关系");

    if import_edges.is_empty() {
        out.push_str("<p class=\"empty\">未检测到导入关系。</p>\n");
    } else {
        let node_map: HashMap<&str, &GraphNode> =
            graph.nodes.iter().map(|n| (n.id.as_str(), n)).collect();

        out.push_str(&format!("<p>共 {} 条导入关系：</p>\n", import_edges.len()));
        out.push_str("<ul class=\"import-list\">\n");

        for edge in &import_edges {
            let src_label = node_map
                .get(edge.source.as_str())
                .and_then(|n| n.file_path.as_deref())
                .unwrap_or(&edge.source);
            let tgt_label = node_map
                .get(edge.target.as_str())
                .and_then(|n| n.file_path.as_deref())
                .unwrap_or(&edge.target);

            out.push_str(&format!(
                "<li>{esc_src} → {esc_tgt}",
                esc_src = esc(src_label),
                esc_tgt = esc(tgt_label)
            ));
            if let Some(ref desc) = edge.description {
                out.push_str(&format!(
                    " <span class=\"edge-desc\">({})</span>",
                    esc(desc)
                ));
            }
            out.push_str("</li>\n");
        }
        out.push_str("</ul>\n");
    }

    html_section_close(out);
}

// ── 5. Dependency Tree ───────────────────────────────────────────────────────

fn html_section_dependency_tree(out: &mut String, graph: &KnowledgeGraph) {
    html_section_open(out, "tree", "目录结构");

    let node_map: HashMap<&str, &GraphNode> =
        graph.nodes.iter().map(|n| (n.id.as_str(), n)).collect();

    // Build a parent→children map from Contains edges
    let mut children: HashMap<&str, Vec<&str>> = HashMap::new();
    for edge in &graph.edges {
        if edge.edge_type == EdgeType::Contains {
            children.entry(&edge.source).or_default().push(&edge.target);
        }
    }

    // Find root nodes (no incoming Contains edge)
    let all_targets: HashSet<&str> = graph
        .edges
        .iter()
        .filter(|e| e.edge_type == EdgeType::Contains)
        .map(|e| e.target.as_str())
        .collect();
    let roots: Vec<&&str> = children
        .keys()
        .filter(|k| !all_targets.contains(**k))
        .collect();

    out.push_str("<pre class=\"ascii-tree\">\n");
    let mut seen = HashSet::new();
    for root in roots {
        if !seen.contains(&root.to_string()) {
            render_html_tree(out, root, &children, &node_map, "", true, &mut seen);
        }
    }
    out.push_str("</pre>\n");

    html_section_close(out);
}

fn render_html_tree(
    out: &mut String,
    node_id: &str,
    children: &HashMap<&str, Vec<&str>>,
    node_map: &HashMap<&str, &GraphNode>,
    prefix: &str,
    is_last: bool,
    seen: &mut HashSet<String>,
) {
    if !seen.insert(node_id.to_string()) {
        return;
    }
    let label = node_map
        .get(node_id)
        .and_then(|n| n.file_path.as_deref())
        .or_else(|| node_map.get(node_id).map(|n| n.name.as_str()))
        .unwrap_or(node_id);

    let connector = if prefix.is_empty() {
        ""
    } else if is_last {
        "└── "
    } else {
        "├── "
    };
    out.push_str(&format!("{}{}{}\n", esc(prefix), connector, esc(label)));

    if let Some(kids) = children.get(node_id) {
        let new_prefix = if prefix.is_empty() {
            "".to_string()
        } else if is_last {
            format!("{}    ", prefix)
        } else {
            format!("{}│   ", prefix)
        };
        for (i, kid) in kids.iter().enumerate() {
            let kid_is_last = i == kids.len() - 1;
            render_html_tree(out, kid, children, node_map, &new_prefix, kid_is_last, seen);
        }
    }
}

// ── 6. Guided Tour ───────────────────────────────────────────────────────────

fn html_section_tour(out: &mut String, graph: &KnowledgeGraph) {
    html_section_open(out, "tour", "引导之旅");

    let node_map: HashMap<&str, &GraphNode> =
        graph.nodes.iter().map(|n| (n.id.as_str(), n)).collect();

    for step in &graph.tour {
        out.push_str(&format!(
            "<div class=\"tour-step\">\n<h3><span class=\"step-num\">{}</span> {}</h3>\n",
            step.order,
            esc(&step.title)
        ));
        out.push_str(&format!("<p>{}</p>\n", esc(&step.description)));

        if let Some(ref lesson) = step.language_lesson {
            out.push_str(&format!(
                "<blockquote class=\"lesson\">💡 {}</blockquote>\n",
                esc(lesson)
            ));
        }

        if !step.node_ids.is_empty() {
            out.push_str("<ul class=\"tour-nodes\">\n");
            for nid in &step.node_ids {
                let label = node_map
                    .get(nid.as_str())
                    .and_then(|n| n.file_path.as_deref())
                    .unwrap_or(nid);
                out.push_str(&format!("<li>{}</li>\n", esc(label)));
            }
            out.push_str("</ul>\n");
        }
        out.push_str("</div>\n");
    }

    html_section_close(out);
}

