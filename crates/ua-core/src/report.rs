/// Determine overall graph complexity from node complexities.
fn complexity_of_graph(graph: &KnowledgeGraph) -> Complexity {
    let mut counts = [0usize; 3]; // Simple, Moderate, Complex
    for node in &graph.nodes {
        match node.complexity {
            Complexity::Simple => counts[0] += 1,
            Complexity::Moderate => counts[1] += 1,
            Complexity::Complex => counts[2] += 1,
        }
    }
    if counts[2] > counts[0] + counts[1] {
        Complexity::Complex
    } else if counts[1] > counts[0] {
        Complexity::Moderate
    } else {
        Complexity::Simple
    }
}

/// Extract line count from summary string like "rust (150 lines, 2 definitions, 1 imports)".
fn extract_line_count(summary: &str) -> Option<&str> {
    let start = summary.find('(')?;
    let end = summary[start..].find(" lines")?;
    Some(&summary[start + 1..start + end])
}

