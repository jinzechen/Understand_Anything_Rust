fn esc(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
}

fn html_header(out: &mut String, graph: &KnowledgeGraph) {
    let title = format!("{} 知识图谱分析报告", esc(&graph.project.name));
    out.push_str("<!DOCTYPE html>\n<html lang=\"zh-CN\">\n<head>\n");
    out.push_str("<meta charset=\"UTF-8\">\n");
    out.push_str("<meta name=\"viewport\" content=\"width=device-width, initial-scale=1.0\">\n");
    out.push_str(&format!("<title>{}</title>\n", title));
    out.push_str("<style>\n");
    out.push_str(include_str!("./report.css"));
    out.push_str("</style>\n</head>\n<body>\n");
    out.push_str(&format!(
        "<header><h1>{}</h1><p class=\"subtitle\">Understand Anything — Knowledge Graph Report</p></header>\n<main>\n",
        title
    ));
}

fn html_footer(out: &mut String) {
    out.push_str("</main>\n<footer><p>Generated by Understand Anything Rust</p></footer>\n</body>\n</html>\n");
}

fn html_section_open(out: &mut String, id: &str, title: &str) {
    out.push_str(&format!("<section id=\"{}\"><h2>{}</h2>\n", id, esc(title)));
}

fn html_section_close(out: &mut String) {
    out.push_str("</section>\n");
}

fn html_node_ref(node: &GraphNode) -> String {
    if let Some(ref fp) = node.file_path {
        format!(
            "<a href=\"#node-{}\" class=\"node-ref\" title=\"{}\">{}</a>",
            esc(&node.id),
            esc(&node.summary),
            esc(fp)
        )
    } else {
        format!(
            "<span class=\"node-ref\" title=\"{}\">{}</span>",
            esc(&node.summary),
            esc(&node.name)
        )
    }
}

